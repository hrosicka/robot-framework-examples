class Calculator:

    def my_sum(self, a, b):
        c = int(a) + int(b)
        return c
    
    def my_difference(self, a, b):
        c = int(a) - int(b)
        return c
    
    def my_product(self, a, b):
        c = int(a) * int(b)
        return c
    
    def my_quotient(self, a, b):
        c = int(a) / int(b)
        return c
        
        